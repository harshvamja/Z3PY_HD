# Function to calculate Hamming Distance between vectors V1 & V2. "target" is for ZeroExt(). 
# [Reference: https://stackoverflow.com/questions/49991650/z3-hamming-distance-between-two-bit-vectors]
def hamming(V1, V2, target):
	h = V1 ^ V2
	s = max(target.bit_length(), V1.size().bit_length())
	return Sum([ZeroExt(s, Extract(i, i, h)) for i in range(V1.size())])

